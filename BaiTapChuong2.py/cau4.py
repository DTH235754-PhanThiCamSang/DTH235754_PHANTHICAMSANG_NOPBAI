'''
Câu 4: Python hỗ trợ những kiểu dữ liệu cơ bản nào?
Python hỗ trợ nhiều kiểu dữ liệu cơ bản như:

Số (Number):

int: Số nguyên (e.g., 1, -5)

float: Số thực (e.g., 3.14, -0.5)

complex: Số phức (e.g., 2 + 3j)

Chuỗi (String):

Dùng để lưu văn bản, ký tự. VD: 'hello', "123"

Boolean (bool):

Chỉ có hai giá trị: True và False

Danh sách (list):

Danh sách có thể thay đổi, chứa nhiều kiểu dữ liệu. VD: [1, 2, 'a']

Tuple (tuple):

Giống list nhưng bất biến (không thay đổi). VD: (1, 2, 3)

Tập hợp (set):

Tập hợp không chứa phần tử trùng lặp. VD: {1, 2, 3}

Từ điển (dict):

Dạng key–value. VD: {'name': 'SAN', 'age': 20}
'''