i1 = 2
i2 = 5
i3 = -3
d1 = 2.0
d2 = 5.0
d3 = -0.5

# Các biểu thức mẫu và giải thích kết quả:

# 1. i1 + (i2 * i3)
result1 = i1 + (i2 * i3)  # 2 + (5 * -3) = 2 + (-15) = -13

# 2. i1 * (i2 + i3)
result2 = i1 * (i2 + i3)  # 2 * (5 + -3) = 2 * 2 = 4

# 3. d1 + (d2 * d3)
result3 = d1 + (d2 * d3)  # 2.0 + (5.0 * -0.5) = 2.0 + (-2.5) = -0.5

# 4. d1 * (d2 + d3)
result4 = d1 * (d2 + d3)  # 2.0 * (5.0 + -0.5) = 2.0 * 4.5 = 9.0

# 5. i2 / i1
result5 = i2 / i1  # 5 / 2 = 2.5

# 6. d2 / d1
result6 = d2 / d1  # 5.0 / 2.0 = 2.5

# 7. i1 / i2
result7 = i1 / i2  # 2 / 5 = 0.4

# 8. d1 / d2
result8 = d1 / d2  # 2.0 / 5.0 = 0.4

# In kết quả và giải thích
print(f"i1 + (i2 * i3) = {result1} -> 2 + (5 * -3) = -13")
print(f"i1 * (i2 + i3) = {result2} -> 2 * (5 + -3) = 4")
print(f"d1 + (d2 * d3) = {result3} -> 2.0 + (5.0 * -0.5) = -0.5")
print(f"d1 * (d2 + d3) = {result4} -> 2.0 * (5.0 + -0.5) = 9.0")
print(f"i2 / i1 = {result5} -> 5 / 2 = 2.5")
print(f"d2 / d1 = {result6} -> 5.0 / 2.0 = 2.5")
print(f"i1 / i2 = {result7} -> 2 / 5 = 0.4")
print(f"d1 / d2 = {result8} -> 2.0 / 5.0 = 0.4")